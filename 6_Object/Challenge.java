package Soo_6_Homework.Week2;

public class Challenge {
    
    double Area (double r) {
        double result = 3.14 * r * r;
        return result;                  //넓이 값 리턴
    }

    double Round (double r) {
        double result = 2 * 3.14 * r;   //둘레 값 리턴
        return result;
    }
}
